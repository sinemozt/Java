package day05_Concatenation;

public class StudentInfo {
    public static void main(String[] args) {
        String studentName = "Muhtar";
        int age = 45;
        char gender = 'M';
        String school_name = "Cydeo";
        String phoneNumber1 = "(123)-456-7891"; // dont forget to add double quote for numbers
        String phoneNumber2 = "(124)-434-7891";
        boolean fullTime = true;
        double GPA = 3.5;

        System.out.println("studentName = " + studentName);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("school_name = " + school_name);
        System.out.println("phoneNumber1 = " + phoneNumber1);
        System.out.println("phoneNumber2 = " + phoneNumber2);
        System.out.println("GPA = " + GPA);
        System.out.println("fullTime = " + fullTime);




    }
}
